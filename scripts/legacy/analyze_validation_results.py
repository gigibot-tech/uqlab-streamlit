"""
Analyze validation experiment results to verify uncertainty disentanglement hypotheses.

This script analyzes the metrics.csv files generated by validation notebooks
to verify that epistemic and aleatoric uncertainty signals are properly disentangled.

Usage:
    python analyze_validation_results.py
"""

import sys
from pathlib import Path
import pandas as pd
import numpy as np
from typing import Dict, List, Tuple

# Signal names for uncertainty quantification
SIGNAL_NAMES = [
    "msp_uncertainty",
    "predictive_entropy", 
    "mutual_info",
    "inverse_coherence",  # Best aleatoric
    "dominance",          # Good epistemic
    "inverse_mass",       # Best epistemic
    "inverse_logit_magnitude"
]

# Logit-based epistemic signals (should respond to dataset size, stable to noise)
EPISTEMIC_SIGNALS = ["inverse_mass", "dominance", "inverse_logit_magnitude"]

# Attribution-based aleatoric signals (should respond to noise, stable to dataset size)
ALEATORIC_SIGNALS = ["inverse_coherence"]

def find_project_root() -> Path:
    """Find the walaris-cen project root."""
    current = Path(__file__).resolve().parent
    if current.name == "walaris-cen":
        return current
    
    # Try parent directories
    for parent in current.parents:
        if parent.name == "walaris-cen":
            return parent
        if (parent / "walaris-cen").exists():
            return parent / "walaris-cen"
    
    raise FileNotFoundError("Could not find walaris-cen project root")

def load_sweep_data(sweep_name: str, project_root: Path) -> pd.DataFrame:
    """Load metrics.csv for a specific sweep."""
    csv_path = project_root / "results" / "validation" / f"{sweep_name}_sweep" / "metrics.csv"
    
    if not csv_path.exists():
        print(f"⚠️  Warning: {csv_path} not found")
        return pd.DataFrame()
    
    df = pd.read_csv(csv_path)
    print(f"✅ Loaded {len(df)} experiments from {sweep_name}_sweep")
    return df

def calculate_signal_trends(df: pd.DataFrame, x_col: str, signal_cols: List[str]) -> Dict[str, Dict]:
    """Calculate trends for each signal across the sweep parameter."""
    results = {}
    
    for signal in signal_cols:
        if signal not in df.columns:
            continue
        
        # Get valid data points
        valid_data = df[[x_col, signal]].dropna()
        if len(valid_data) < 2:
            continue
        
        x_values = valid_data[x_col].values
        y_values = valid_data[signal].values
        
        # Calculate statistics
        mean_auroc = np.mean(y_values)
        std_auroc = np.std(y_values)
        min_auroc = np.min(y_values)
        max_auroc = np.max(y_values)
        range_auroc = max_auroc - min_auroc
        
        # Calculate trend using Spearman-like rank correlation
        x_ranks = pd.Series(x_values).rank(method='average')
        y_ranks = pd.Series(y_values).rank(method='average')
        correlation = x_ranks.corr(y_ranks)
        
        # Determine trend direction
        if correlation > 0.3:
            trend = "↗️ Increases"
        elif correlation < -0.3:
            trend = "↘️ Decreases"
        else:
            trend = "→ Stable"
        
        results[signal] = {
            'mean': mean_auroc,
            'std': std_auroc,
            'min': min_auroc,
            'max': max_auroc,
            'range': range_auroc,
            'correlation': correlation,
            'trend': trend,
            'n_points': len(valid_data)
        }
    
    return results

def analyze_epistemic_sweep(df: pd.DataFrame) -> Dict[str, Dict]:
    """Analyze dataset size sweep (epistemic uncertainty)."""
    if df.empty:
        return {}
    
    # The x-axis is dataset_size (under_train_per_class)
    x_col = 'dataset_size'
    
    # Get epistemic AUROC columns for each signal
    signal_cols = [f"{sig}_epistemic_auroc" for sig in SIGNAL_NAMES]
    
    return calculate_signal_trends(df, x_col, signal_cols)

def analyze_aleatoric_sweep(df: pd.DataFrame) -> Dict[str, Dict]:
    """Analyze label noise sweep (aleatoric uncertainty)."""
    if df.empty:
        return {}
    
    # The x-axis is noise_rate
    x_col = 'noise_rate'
    
    # Get aleatoric AUROC columns for each signal
    signal_cols = [f"{sig}_aleatoric_auroc" for sig in SIGNAL_NAMES]
    
    return calculate_signal_trends(df, x_col, signal_cols)

def print_sweep_analysis(sweep_name: str, results: Dict[str, Dict], signal_type: str):
    """Print formatted analysis results."""
    if not results:
        print(f"   ❌ No data available for {sweep_name} sweep")
        return
    
    print(f"\n{'='*80}")
    print(f"📊 {sweep_name.upper()} SWEEP ANALYSIS")
    print(f"   Testing: {signal_type} uncertainty signals")
    print(f"{'='*80}\n")
    
    # Sort by mean AUROC (descending)
    sorted_signals = sorted(results.items(), key=lambda x: x[1]['mean'], reverse=True)
    
    print(f"{'Signal':<30} {'Mean AUROC':<12} {'Range':<10} {'Trend':<15} {'Quality'}")
    print("-" * 90)
    
    for signal_full, stats in sorted_signals:
        # Extract signal name (remove _epistemic_auroc or _aleatoric_auroc suffix)
        signal = signal_full.replace('_epistemic_auroc', '').replace('_aleatoric_auroc', '')
        
        # Determine if this is an expected signal
        is_epistemic = signal in EPISTEMIC_SIGNALS
        is_aleatoric = signal in ALEATORIC_SIGNALS
        
        # Quality assessment
        if signal_type == "epistemic":
            if is_epistemic and stats['mean'] > 0.75 and stats['correlation'] > 0.3:
                quality = "⭐⭐⭐ EXCELLENT"
            elif is_epistemic and stats['mean'] > 0.65:
                quality = "⭐⭐ GOOD"
            elif stats['correlation'] > 0.3:
                quality = "⭐ MODERATE"
            else:
                quality = "WEAK"
        else:  # aleatoric
            if is_aleatoric and stats['mean'] > 0.65 and stats['correlation'] > 0.3:
                quality = "⭐⭐⭐ EXCELLENT"
            elif is_aleatoric and stats['mean'] > 0.55:
                quality = "⭐⭐ GOOD"
            elif stats['correlation'] > 0.3:
                quality = "⭐ MODERATE"
            else:
                quality = "WEAK"
        
        marker = " 🎯" if (is_epistemic and signal_type == "epistemic") or (is_aleatoric and signal_type == "aleatoric") else ""
        
        print(f"{signal:<30} {stats['mean']:.4f}       {stats['range']:.4f}     {stats['trend']:<15} {quality}{marker}")
    
    print()

def print_hypothesis_verification(epistemic_results: Dict, aleatoric_results: Dict):
    """Print hypothesis verification summary."""
    print("\n" + "="*80)
    print("🔬 HYPOTHESIS VERIFICATION SUMMARY")
    print("="*80)
    
    # Check epistemic signals
    print("\n✅ EPISTEMIC UNCERTAINTY DISENTANGLEMENT:")
    print("   Expected: inverse_mass & dominance should:")
    print("   • Show HIGH AUROC (>0.75) in dataset size sweep")
    print("   • Show INCREASING trend with more data")
    print("   • Show STABLE behavior in noise sweep")
    print()
    
    epistemic_verified = True
    for signal in EPISTEMIC_SIGNALS:
        signal_key = f"{signal}_epistemic_auroc"
        if signal_key in epistemic_results:
            stats = epistemic_results[signal_key]
            auroc_ok = stats['mean'] > 0.65
            trend_ok = stats['correlation'] > 0.2
            
            status = "✅" if (auroc_ok and trend_ok) else "⚠️"
            print(f"   {status} {signal}: AUROC={stats['mean']:.3f}, {stats['trend']}")
            
            if not (auroc_ok and trend_ok):
                epistemic_verified = False
        else:
            print(f"   ❌ {signal}: No data")
            epistemic_verified = False
    
    # Check aleatoric signals
    print("\n✅ ALEATORIC UNCERTAINTY DISENTANGLEMENT:")
    print("   Expected: inverse_coherence should:")
    print("   • Show HIGH AUROC (>0.65) in noise sweep")
    print("   • Show INCREASING trend with more noise")
    print("   • Show STABLE behavior in dataset size sweep")
    print()
    
    aleatoric_verified = True
    for signal in ALEATORIC_SIGNALS:
        signal_key = f"{signal}_aleatoric_auroc"
        if signal_key in aleatoric_results:
            stats = aleatoric_results[signal_key]
            auroc_ok = stats['mean'] > 0.55
            trend_ok = stats['correlation'] > 0.2
            
            status = "✅" if (auroc_ok and trend_ok) else "⚠️"
            print(f"   {status} {signal}: AUROC={stats['mean']:.3f}, {stats['trend']}")
            
            if not (auroc_ok and trend_ok):
                aleatoric_verified = False
        else:
            print(f"   ❌ {signal}: No data")
            aleatoric_verified = False
    
    # Overall verdict
    print("\n" + "="*80)
    print("🎯 OVERALL VERDICT:")
    print("="*80)
    
    if epistemic_verified and aleatoric_verified:
        print("✅ ✅ ✅ HYPOTHESIS FULLY CONFIRMED!")
        print("   Uncertainty disentanglement is working correctly.")
        print("   Both epistemic and aleatoric signals show expected behavior.")
    elif epistemic_verified:
        print("✅ ⚠️ HYPOTHESIS PARTIALLY CONFIRMED")
        print("   Epistemic disentanglement works correctly.")
        print("   Aleatoric disentanglement needs improvement.")
    elif aleatoric_verified:
        print("⚠️ ✅ HYPOTHESIS PARTIALLY CONFIRMED")
        print("   Aleatoric disentanglement works correctly.")
        print("   Epistemic disentanglement needs improvement.")
    else:
        print("❌ HYPOTHESIS NOT CONFIRMED")
        print("   Uncertainty disentanglement is not working as expected.")
        print("   Both epistemic and aleatoric signals need improvement.")
    
    print()

def main():
    """Main analysis function."""
    try:
        project_root = find_project_root()
        print(f"📁 Project root: {project_root}")
        print()
        
        # Load both sweeps
        dataset_size_df = load_sweep_data("dataset_size", project_root)
        label_noise_df = load_sweep_data("label_noise", project_root)
        print()
        
        # Analyze epistemic sweep (dataset size)
        epistemic_results = analyze_epistemic_sweep(dataset_size_df)
        print_sweep_analysis("Dataset Size", epistemic_results, "epistemic")
        
        # Analyze aleatoric sweep (label noise)
        aleatoric_results = analyze_aleatoric_sweep(label_noise_df)
        print_sweep_analysis("Label Noise", aleatoric_results, "aleatoric")
        
        # Print hypothesis verification
        print_hypothesis_verification(epistemic_results, aleatoric_results)
        
    except Exception as e:
        print(f"❌ Error: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)

if __name__ == "__main__":
    main()

